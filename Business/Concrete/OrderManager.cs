namespace Business.Concrete
{
    public class OrderManager : IOrderService
    {
        IOrderDal _orderDal;
        public OrderManager(IOrderDal orderDal)
        {
            _orderDal = orderDal;
        }

        public List<OrderDetailDto> GetOrderDetail()
        {
            return _orderDal.GetOrderDetail();
        }
    }
